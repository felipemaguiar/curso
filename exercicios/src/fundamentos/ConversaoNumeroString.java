package fundamentos;

public class ConversaoNumeroString {
	public static void main(String[] args) {
		Integer num1 = 10000;
		System.out.println(num1.toString()); // Convertento o num1 para string
		
		System.out.println(num1.toString().length());
		
		int num2 = 2222;
		System.out.println(Integer.toString(num2)); // Convertendo o num2 para string diretamente
		System.out.println(Integer.toString(num2).length());
		
		
		
		
		
	}
}
